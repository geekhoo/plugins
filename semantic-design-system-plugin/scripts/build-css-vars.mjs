#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { loadTokenTree, collectTokens, resolveAlias, tokenValueToCss, cssVarName, projectPath } from './token-utils.mjs';

const tokenDir = projectPath(process.argv[2] || process.env.DEFAULT_TOKEN_DIR || 'tokens');
const outputFile = projectPath(process.argv[3] || process.env.DEFAULT_CSS_OUTPUT_FILE || 'src/styles/tokens.css');
const prefix = process.argv[4] || process.env.DEFAULT_CSS_PREFIX || 'ds';

try {
  const { tree } = loadTokenTree(tokenDir);
  const { tokens } = collectTokens(tree);
  const lines = [];
  lines.push('/* Generated by semantic-design-system plugin. Do not edit manually. */');
  lines.push(':root {');

  for (const [tokenPath, token] of [...tokens.entries()].sort(([a], [b]) => a.localeCompare(b))) {
    if (tokenPath.startsWith('$')) continue;
    if (!(tokenPath.startsWith('semantic.') || tokenPath.startsWith('component.'))) continue;
    const resolved = resolveAlias(token.node.$value, tokens, [tokenPath]);
    const css = tokenValueToCss(resolved);
    if (css.includes('{')) continue;
    lines.push(`  ${cssVarName(tokenPath, prefix)}: ${css};`);
  }

  lines.push('}');
  lines.push('');

  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
  fs.writeFileSync(outputFile, lines.join('\n'), 'utf8');

  console.log(JSON.stringify({ ok: true, tokenDir, outputFile, prefix, variables: lines.length - 3 }, null, 2));
} catch (error) {
  console.error(JSON.stringify({ ok: false, tokenDir, outputFile, error: error.message }, null, 2));
  process.exit(1);
}
