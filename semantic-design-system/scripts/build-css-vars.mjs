#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import {
  walkFiles,
  readJsonLike,
  deepMerge,
  collectTokens,
  resolveAlias,
  tokenValueToCss,
  cssVarName,
  projectPath,
  TOKEN_FILE_RE,
  isThemeFile,
  DEFAULT_TOKEN_DIR
} from './token-utils.mjs';

const tokenDir = projectPath(process.argv[2] || DEFAULT_TOKEN_DIR);
const outputFile = projectPath(process.argv[3] || process.env.DEFAULT_CSS_OUTPUT_FILE || 'src/styles/tokens.css');
const prefix = process.argv[4] || process.env.DEFAULT_CSS_PREFIX || 'ds';

// Only `semantic.*` and `component.*` tokens become CSS custom properties.
// Primitives stay an implementation detail and are inlined via alias resolution.
function isExposed(tokenPath) {
  return tokenPath.startsWith('semantic.') || tokenPath.startsWith('component.');
}

// Resolve every exposed token in `context` to a final CSS string, keyed by token
// path. Tokens that fail to resolve or stay unresolved are skipped.
function resolveExposed(context) {
  const map = new Map();
  for (const [tokenPath, token] of context) {
    if (tokenPath.startsWith('$') || !isExposed(tokenPath)) continue;
    let css;
    try {
      css = tokenValueToCss(resolveAlias(token.node.$value, context, [tokenPath]));
    } catch {
      continue;
    }
    if (css.includes('{')) continue; // unresolved alias; skip rather than emit garbage
    map.set(tokenPath, css);
  }
  return map;
}

function declarationsFrom(resolvedMap) {
  return [...resolvedMap.keys()]
    .sort((a, b) => a.localeCompare(b))
    .map(p => `  ${cssVarName(p, prefix)}: ${resolvedMap.get(p)};`);
}

function block(selector, declarations, indent = '') {
  if (!declarations.length) return [];
  const out = [`${indent}${selector} {`];
  for (const d of declarations) out.push(`${indent}${d}`);
  out.push(`${indent}}`);
  return out;
}

function themeName(file) {
  return path.basename(file).replace(/\.tokens(\.jsonc?)?$/, '');
}

try {
  const files = walkFiles(tokenDir, f => TOKEN_FILE_RE.test(f));
  const baseFiles = files.filter(f => !isThemeFile(f));
  const themeFiles = files.filter(isThemeFile);

  // Base layer: everything that is not a theme override.
  let baseTree = {};
  for (const f of baseFiles) baseTree = deepMerge(baseTree, readJsonLike(f));
  const baseResolved = resolveExposed(collectTokens(baseTree).tokens);

  const out = [];
  out.push('/* Generated by semantic-design-system plugin. Do not edit manually. */');

  let variableCount = 0;
  const rootDecls = declarationsFrom(baseResolved);
  variableCount += rootDecls.length;
  out.push(...block(':root', rootDecls));

  // Each theme file is emitted into its own selector. A theme declares only the
  // tokens whose *resolved* value differs from the base — so an override of a
  // primitive or semantic also re-emits every component/semantic token that
  // aliases it (the inlined cascade cannot propagate on its own). Themes named
  // `light`/`dark` additionally honor the OS `prefers-color-scheme` when no
  // explicit data-theme attribute is set, while an explicit `[data-theme]` wins.
  const themes = [];
  for (const file of themeFiles) {
    const name = themeName(file);
    const themeTree = readJsonLike(file);
    const themeResolved = resolveExposed(collectTokens(deepMerge(baseTree, themeTree)).tokens);
    const changed = new Map();
    for (const [p, css] of themeResolved) {
      if (baseResolved.get(p) !== css) changed.set(p, css);
    }
    const decls = declarationsFrom(changed);
    if (!decls.length) continue;
    variableCount += decls.length;
    themes.push({ name, decls });
  }

  for (const { name, decls } of themes) {
    out.push('');
    out.push(...block(`[data-theme="${name}"]`, decls));
    if (name === 'light' || name === 'dark') {
      out.push('');
      out.push(`@media (prefers-color-scheme: ${name}) {`);
      out.push(...block(':root:not([data-theme])', decls, '  '));
      out.push('}');
    }
  }

  out.push('');

  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
  fs.writeFileSync(outputFile, out.join('\n'), 'utf8');

  console.log(JSON.stringify({
    ok: true,
    tokenDir,
    outputFile,
    prefix,
    variables: variableCount,
    themes: themes.map(t => t.name)
  }, null, 2));
} catch (error) {
  console.error(JSON.stringify({ ok: false, tokenDir, outputFile, error: error.message }, null, 2));
  process.exit(1);
}
